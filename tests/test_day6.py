import pytest

from solutions.day6.solution import part_1, part_2


@pytest.mark.parametrize(
    "datastream,marker_index",
    [
        ("mjqjpqmgbljsphdztnvjfqwrcgsmlb", 7),
        ("bvwbjplbgvbhsrlpgdmjqwftvncz", 5),
        ("nppdvjthqldpwncqszvftbrmjlhg", 6),
        ("nznrnfrfntjfmvfwmzdfjlvtqnbhcprsg", 10),
        ("zcfzfwzzqfrljwzlrfnpqdbhtmscgvjw", 11),
    ],
)
def test_part_1_returns_correct_value(datastream: str, marker_index: int) -> None:
    assert part_1(datastream) == marker_index


@pytest.mark.parametrize(
    "datastream,marker_index",
    [
        ("mjqjpqmgbljsphdztnvjfqwrcgsmlb", 19),
        ("bvwbjplbgvbhsrlpgdmjqwftvncz", 23),
        ("nppdvjthqldpwncqszvftbrmjlhg", 23),
        ("nznrnfrfntjfmvfwmzdfjlvtqnbhcprsg", 29),
        ("zcfzfwzzqfrljwzlrfnpqdbhtmscgvjw", 26),
    ],
)
def test_part_2_returns_correct_value(datastream: str, marker_index: int) -> None:
    assert part_2(datastream) == marker_index
